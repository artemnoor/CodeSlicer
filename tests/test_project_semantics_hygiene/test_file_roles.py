from project_semantics_hygiene import FileRole, FileRoleClassifier, ProjectFile


def test_generated_path_detection():
    fc = FileRoleClassifier().classify_path("frontend/src/__generated__/api.generated.ts")
    assert fc.role is FileRole.GENERATED
    assert fc.is_generated is True
    assert fc.language == "typescript"
    assert fc.confidence >= 0.9


def test_generated_content_marker_detection():
    content = "// Code generated by tool. DO NOT EDIT\nexport const x = 1;"
    fc = FileRoleClassifier().classify_path("src/client.ts", content)
    assert fc.role is FileRole.GENERATED
    assert any("generated marker" in r for r in fc.reasons)


def test_pytest_vitest_go_java_tests():
    classifier = FileRoleClassifier()
    assert classifier.classify_path("tests/test_orders.py").role is FileRole.TEST
    assert classifier.classify_path("frontend/src/order.test.tsx").role is FileRole.TEST
    assert classifier.classify_path("store/user_test.go").role is FileRole.TEST
    assert classifier.classify_path("src/UserServiceTest.java").role is FileRole.TEST


